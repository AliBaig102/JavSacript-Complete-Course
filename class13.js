// Location Object
// - location.href => gives the URL of the current page
// - location.protocol => gives the protocol of the current page
// - location.host => gives the host of the current page
// - location.hostname => gives the hostname of the current page
// - location.port => gives the port of the current page
// - location.pathname => gives the pathname of the current page
// - location.search => gives the search of the current page
// - location.hash => gives the hash of the current page

