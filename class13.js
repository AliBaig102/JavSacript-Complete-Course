// Navigator Object
// - navigator.appName => gives the name of the browser
// - navigator.appVersion => gives the version of the browser
// - navigator.userAgent => gives the name and version of the browser
// - navigator.platform => gives the platform of the browser
// - navigator.language => gives the language of the browser
// - navigator.cookieEnabled => checks if cookies are enabled in the browser

