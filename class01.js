// what is programming language?
// - A programming language is a formal language that is used to create programs for computers.

// What is a high-level, interpreted language?
// - A high-level, interpreted language is a programming language that is designed to be easier to read and write.

// What is a scripting language?
// - A scripting language is a programming language that is designed to be used in a web browser.

